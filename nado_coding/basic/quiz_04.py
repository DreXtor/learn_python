from random import *
from random import *      # import random

call = 0                  # 매칭 횟수
get_call = 0              # 탑승 손님수

while call < 50:      # 매칭 횟수 50번
    call += 1         # 매칭 횟수 + 1
    time = randrange(5, 50)       # 소요시간 5분 ~ 10분 사이의 난수
    if 5 <= time <= 15:           # 5분 ~ 10분 사이라면
        print("[O] {0}번째 손님 (소요시간 : {1}분".format(call, time))        # 문장 출력
        get_call += 1     # 탑승 손님 카운트
    else:         # 5분 ~ 10분이 아니라면
        print("[ ] {0}번째 손님 (소요시간 : {1}분".format(call, time))        # 문장 출력

print("총 탐승 승객 : {0} 분".format(get_call))

