# 조건 1 : 승객별 운행 소요시간은 5분 - 50분 사이의 난수로 정해집니다.
# 조건 2 : 당신은 소요시간 5분 - 15분 사이의 승객만 매칭해야합니다.

# (출력문 예제)
# [O] 1번째 손님 (소요시간 : 15분)
# [ ] 2번째 손님 (소요시간 : 54분)
# [O] 3번째 손님 (소요시간 : 5분)
# ...
# [ ] 50번째 손님 (소요시간 : 30분)

